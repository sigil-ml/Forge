def add_one(x):
    """
    Adds one to a number and returns the result.

    :param x: Number to incremented
    :type x: int
    :raise ValueError: if x is non-numeric
    :return: incremented value
    :rtype: int
    """

    return x + 1


def subtract_one(x):
    """
    Subtracts one to a number and returns the result.

    :param x: Number to decremented
    :type x: int
    :raise ValueError: if x is non-numeric
    :return: decremented value
    :rtype: int
    """
    return x - 1


def multiply_one(x):
    """
    Multiplies a number by one and returns the result.

    :param x: Number to be multiplied
    :type x: int
    :raise ValueError: if x is non-numeric
    :return: multiplied value
    :rtype: int
    """
    return x * 1


def divide_one(x):
    """
    Divides a number by one and returns the result.

    :param x: Number to be divided
    :type x: int
    :raise ValueError: if x is non-numeric
    :return: divided value
    :rtype: int
    """

    return x / 1


def squared(x):
    """
    Squares a number and returns the result.

    :param x: Number to be divided
    :type x: int
    :raise ValueError: if x is non-numeric
    :return: divided value
    :rtype: int
    """

    return x * x
